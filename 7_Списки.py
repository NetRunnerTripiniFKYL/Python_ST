print (f"#8 \nВывод: {list1} \n")

list1.insert(1, 20) #Вставляет элемент на заданную позицию по индексу (но двигает все остальные вправо на одну единицу) (обраьно вставляем 20 на вторую по счету позицию (по индексу на 1))
print (f"#9 \nВывод: {list1} \n")

list1.sort() #Упорядочиваем список по возрастанию значения элементов (ничего не изменилось (у нас все и так упорядочено))
print (f"#10 \nВывод: {list1} \n")

list1.reverse() #Работает как sort только в обратном порядке (Упорядочивает по убыванию)
print (f"#11 \nВывод: {list1} \n")

