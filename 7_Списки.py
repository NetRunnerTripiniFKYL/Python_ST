print (f"#2 \nВывод: {list1[5]} \n") #Вытаскиваем отдельный элемент по индексу 

print (f"#3 \nВывод: {list[3 : 5]} \n") #Группу элементов при помощи среза

list1[0] = 11 #Можно присваивать (менять) значение элемента по индексу
print (f"#4 \nВывод: {list1} \n")

list1.append(120) #Добавляем новый элемент в конец списка
print (f"#5 \nВывод: {list1} \n")

elem_from_list = list1.pop(1) #Извлекаем элемент по индексу и присваиваем это значение переменной elem_from_list
print (f"#6 \nВывод: {list1} \n")
print (f"#7 \nВывод: {elem_from_list} \n")

