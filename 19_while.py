# С помощью цикла while можно сделать так, что скрипт сам будет запрашивать пароль заново, если он не соответствует требованиям.

print (" \n#2")

username = input("Введите имя пользователя: ")
password = input("Введите пароль: ")
mail = input ("Введите ваш почтовый ящик: ")
password_correct = False

while not password_correct:
    if len(password) < 8:
        print("Пароль слишком короткий\n")
        password = input("Введите пароль еще раз: ")
    elif username in password:
        print("Пароль содержит имя пользователя\n")
        password = input("Введите пароль еще раз: ")
    else:
        print ("User {0} зарегистрирован! Логин и пароль продублированы на почту {1}".format(username, mail))
        password_correct = True