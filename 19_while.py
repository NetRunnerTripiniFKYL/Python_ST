#Цикл while - это еще одна разновидность цикла в Python

print (" \n#1")

"""
В цикле while, как и в выражении if, надо писать условие. Если условие истинно, выполняются действия внутри блока while. 
При этом, в отличие от if, после выполнения кода в блоке, while возвращается в начало цикла.
При использовании циклов while необходимо обращать внимание на то, будет ли достигнуто такое состояние, при котором условие цикла будет ложным.
"""

num = 5

while num > 0: # Пока num будет больше 0
    print (num)
    num -= 1 # num = num - 1

"""
Сначала создается переменная а со значением 5.
Затем, в цикле while указано условие a > 0. 
То есть, пока значение а больше 0, будут выполняться действия в теле цикла. В данном случае, будет выводиться значение переменной а.
Кроме того, в теле цикла при каждом прохождении значение а становится на единицу меньше.
"""

